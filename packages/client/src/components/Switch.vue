<script setup lang="ts">
import { ref } from 'vue'

defineProps<{ text: string }>()

const value = ref(false)

const emit = defineEmits(['switch-clicked'])

function onChange() {
  emit('switch-clicked', value.value)
}

</script>

<template>
  <label class="switch">
    <input type="checkbox" v-model="value" @change="onChange">
    <span class="slider round"></span>
    <p class="show">{{ text }}</p>
  </label>
</template>

